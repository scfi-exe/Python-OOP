# a CLASS is a blueprint for creating objects. here's the basic syntax for creating a class in Python:
class SuperHero:
    def __init__(self, name: str, power: str, health: int, speed: int):
        self.name = name
        self.power = power
        self.health = health
        self.speed = speed


# In Python, a Class is is defined by the keyword Class, followed by the Name of the class and then a colon:
# The __init__() method is a special method that belongs to the class. It creates the object and initializes its attributes
