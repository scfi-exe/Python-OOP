# WHAT IS ENCAPSULATION?
# ENCAPSULATION is the concept of wrapping data and methods that work on the data within one unit, called a class.
# In addition, it restricts some of the object's components.
# The purpose is to hide some of the internal details of an object, and only expose the necessary parts to the outside world.
# A real-world analogy of this is car. A car is a complex system with many moving parts.
# However, as a driver, you don't need to know how the car works internally. You only need to know how to operate it.


# KEY POINTS ABOUT PUBLIC ATTRIBUTES
# PUBLIC ATTRIBUTES are accessible from anywhere in the code
# They're defined without any special naming convention
# They offer direct access to object properties
# They are suitable for simple, straightforward data storage.
# No special methods are needed to access or modify them.
